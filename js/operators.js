//Operadores de comparacion 

// == iguales solo en valor: Equivalente 
// === igual en valor y tipos de datos: exactamente iguales
// != no es igual o diferencia 
// > mayor que 
// < menor que 
// >= mayor o igual que 
// <= menor o igual que

console.log(3 > 2) //true
console.log(3 >= 2) // true
console.log(3 < 2) // false
console.log(2 < 3) // true
console.log(2 <= 3) //true 
console.log(3 == 2) // false
console.log(3 != 2)//
console.log(3 === '3') //false

